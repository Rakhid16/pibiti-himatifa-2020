def volume_kubus(sisi):
  return sisi ** 3

def luas_permukaan_kubus(sisi):
  return sisi * sisi * 6

def volume_balok(panjang, lebar, tinggi):
  return panjang * lebar * tinggi

def luas_permukaan_balok(panjang, lebar, tinggi):
  return 2 * (panjang * lebar) + 2 * (panjang * tinggi) + 2 * (lebar * tinggi)

def volume_bola(diameter):
  return 4/3 * 22/7 * ((diameter/2) ** 3)

def luas_permukaan_bola(diameter):
  return 4 * 22/7 * ((diameter/2) ** 2)